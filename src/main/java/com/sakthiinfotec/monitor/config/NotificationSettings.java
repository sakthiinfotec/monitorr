package com.sakthiinfotec.monitor.config;

/**
 * Notification settings configuration
 * 
 * @author Sakthi
 */
public class NotificationSettings {
	private String host;
	private int port;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
}
